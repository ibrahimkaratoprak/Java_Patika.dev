package PatikaJava;

import java.util.Scanner;

public class Elmasdesign {
    public static void main(String[] args) {
        int size = 5;

        // Sol üst köşe kadar yıldızları yazdır
        for (int i = 0; i < size; i++) {
            for (int j = 0; j < size - i; j++) {
                System.out.print(" ");
            }
            for (int j = 0; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }

        // Sağ alt köşe kadar yıldızları yazdır
        for (int i = size - 2; i >= 0; i--) {
            for (int j = 0; j < size - i; j++) {
                System.out.print(" ");
            }
            for (int j = 0; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }}
